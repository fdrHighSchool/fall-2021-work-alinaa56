public class PerfectDay {

    public static void intro() {
      System.out.println("Hello, here is my perfect day!");
    }

    public static void morning() {
        System.out.println("Wake up and having a breakfast watching the nature around");
        System.out.println("Go hiking with my best friend");
    }

    public static void afternoon() {
        System.out.println("After that go swimming in the clean lake or river");
        System.out.println("Come back to house, cook a healthy lunch together and enjoy it");
    }

    public static void evening() {
        System.out.println("Then, i would like to spend time on drawing");
        System.out.println("And watch old horror movie with my friend+eating popcorn!");
        System.out.println("Before going to sleep - drink a cup of tea,take a bath and talk about this perfect day with my friend");
    }

    public static void night() {
      System.out.println("Finally,go to sleep in a warm and soft bed and see spooky dreams");
    }

    public static void main(String[] args) {
        intro();
        morning();
        afternoon();
        evening();
        night();

        System.out.println();
    }
}

